class AlertLogger:
    """تسجيل التنبيهات"""
    
    def __init__(self, alerts_dir: str = "reports/alerts"):
        self.alerts_dir = alerts_dir
        os.makedirs(alerts_dir, exist_ok=True)
    
    def log_alert(
        self,
        component: str,
        level: int,
        message: str,
        psi: float,
        risk: float
    ) -> str:
        """تسجيل تنبيه جديد"""
        now = datetime.datetime.now()
        date_str = now.strftime("%Y-%m-%d")
        filename = f"{self.alerts_dir}/alerts_{date_str}.txt"
        
        alert_entry = f"""
[{now.strftime("%H:%M:%S")}] COMPONENT: {component}
    LEVEL: {level}
    PSI: {psi:.3f}
    RISK: {risk:.3f}
    MESSAGE: {message}
{'-' * 50}
"""
        
        # إضافة إلى ملف التنبيهات
        with open(filename, 'a', encoding='utf-8') as f:
            f.write(alert_entry)
        
        return filename


