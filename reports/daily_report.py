import datetime
import json
import os
from typing import Dict, List, Optional


class DailyReportGenerator:
    """توليد التقارير اليومية للنظام"""
    
    def __init__(self, reports_dir: str = "reports/daily"):
        self.reports_dir = reports_dir
        os.makedirs(reports_dir, exist_ok=True)
    
    def generate(
        self,
        system_name: str,
        metrics: Dict,
        alerts: List[Dict],
        summary: str
    ) -> str:
        """
        توليد تقرير يومي
        
        Args:
            system_name: اسم النظام
            metrics: مقاييس اليوم (متوسط Ψ، max Ψ، إلخ)
            alerts: قائمة التنبيهات
            summary: ملخص النص
        
        Returns:
            مسار ملف التقرير
        """
        today = datetime.datetime.now()
        date_str = today.strftime("%Y-%m-%d")
        filename = f"{self.reports_dir}/{system_name}_{date_str}.txt"
        
        # خطوط فاصل
        line = "=" * 70
        small_line = "-" * 70
        
        content = f"""
{line}
                    ENTRO-AI DAILY REPORT
{line}

System: {system_name}
Date: {today.strftime("%Y-%m-%d")}
Time: {today.strftime("%H:%M:%S")}
Week: {today.strftime("%W")}
Month: {today.strftime("%B")}

{small_line}

📊 DAILY METRICS
{small_line}

┌─────────────────────────────────────────────────────────────────┐
│ Metric                    │ Value      │ Status                 │
├─────────────────────────────────────────────────────────────────┤
│ Average Ψ                 │ {metrics.get('avg_psi', 0):8.3f}    │ {self._status_icon(metrics.get('avg_psi', 0))}                    │
│ Maximum Ψ                 │ {metrics.get('max_psi', 0):8.3f}    │ {self._status_icon(metrics.get('max_psi', 0))}                    │
│ Minimum Ψ                 │ {metrics.get('min_psi', 0):8.3f}    │                         │
│ Average Risk              │ {metrics.get('avg_risk', 0):8.3f}   │ {self._risk_icon(metrics.get('avg_risk', 0))}                    │
│ Peak Risk                 │ {metrics.get('peak_risk', 0):8.3f}  │ {self._risk_icon(metrics.get('peak_risk', 0))}                    │
│ Time at Risk > 0.5        │ {metrics.get('time_at_risk', 0):8.1f} min │                         │
│ Components Active         │ {metrics.get('active_components', 0):8d}    │                         │
│ Components at Warning     │ {metrics.get('warning_components', 0):8d}   │ ⚠️                         │
│ Components at Critical    │ {metrics.get('critical_components', 0):8d}  │ 🔴                         │
│ EDT Interventions         │ {metrics.get('edt_interventions', 0):8d}    │                         │
└─────────────────────────────────────────────────────────────────┘

{small_line}

⚠️ ALERTS ({len(alerts)})
{small_line}

"""
        
        if alerts:
            for i, alert in enumerate(alerts, 1):
                content += f"""
[{i}] Time: {alert.get('time', 'N/A')}
    Component: {alert.get('component', 'N/A')}
    Level: {alert.get('level', 'N/A')}
    Message: {alert.get('message', 'N/A')}
    Ψ: {alert.get('psi', 0):.3f}
"""
        else:
            content += "\n    ✅ No alerts recorded today\n"
        
        content += f"""

{small_line}

📈 SUMMARY
{small_line}

{summary}

{small_line}

📋 RECOMMENDATIONS
{small_line}

{self._generate_recommendations(metrics, alerts)}

{small_line}

🔧 SYSTEM STATUS
{small_line}

Overall Health: {self._health_status(metrics, alerts)}
Risk Level: {self._overall_risk(metrics)}
EDT Status: {'Active' if metrics.get('edt_interventions', 0) > 0 else 'Standby'}

{line}
Report generated by ENTRO-AI v2.0.0
Builds on ENTROPIA (E-LAB-01) — DOI: 10.5281/zenodo.19416737
{line}
"""
        
        # كتابة الملف
        with open(filename, 'w', encoding='utf-8') as f:
            f.write(content)
        
        print(f"✅ Daily report saved: {filename}")
        return filename
    
    def _status_icon(self, psi: float) -> str:
        """أيقونة الحالة حسب Ψ"""
        if psi >= 1.5:
            return "🔴"
        elif psi >= 1.0:
            return "🟠"
        elif psi >= 0.5:
            return "🟡"
        else:
            return "🟢"
    
    def _risk_icon(self, risk: float) -> str:
        """أيقونة المخاطر"""
        if risk >= 0.8:
            return "🔴"
        elif risk >= 0.5:
            return "🟠"
        elif risk >= 0.3:
            return "🟡"
        else:
            return "🟢"
    
    def _health_status(self, metrics: Dict, alerts: List) -> str:
        """تقييم الصحة العامة"""
        if metrics.get('critical_components', 0) > 0:
            return "🔴 CRITICAL - Immediate attention required"
        elif metrics.get('warning_components', 0) > 0:
            return "🟡 WARNING - Monitor closely"
        elif metrics.get('max_psi', 0) > 1.0:
            return "🟠 ELEVATED - Investigate"
        else:
            return "🟢 HEALTHY - Normal operation"
    
    def _overall_risk(self, metrics: Dict) -> str:
        """مستوى المخاطر الإجمالي"""
        peak_risk = metrics.get('peak_risk', 0)
        if peak_risk >= 1.0:
            return "IMMINENT"
        elif peak_risk >= 0.6:
            return "WARNING"
        elif peak_risk >= 0.3:
            return "CAUTION"
        else:
            return "STABLE"
    
    def _generate_recommendations(self, metrics: Dict, alerts: List) -> str:
        """توليد توصيات بناءً على المقاييس"""
        recommendations = []
        
        if metrics.get('critical_components', 0) > 0:
            recommendations.append("🔴 Schedule immediate maintenance for critical components")
        
        if metrics.get('warning_components', 0) > 0:
            recommendations.append("🟡 Investigate warning-level components")
        
        if metrics.get('max_psi', 0) > 1.5:
            recommendations.append("⚠️ Consider load reduction or capacity increase")
        
        if metrics.get('time_at_risk', 0) > 60:
            recommendations.append("⏰ Prolonged elevated risk - review system architecture")
        
        if metrics.get('edt_interventions', 0) > 10:
            recommendations.append("🔄 Frequent EDT interventions - consider scaling")
        
        if not recommendations:
            recommendations.append("✅ No immediate actions required")
        
        return "\n".join(f"    {r}" for r in recommendations)


class WeeklyReportGenerator:
    """توليد التقارير الأسبوعية"""
    
    def __init__(self, reports_dir: str = "reports/weekly"):
        self.reports_dir = reports_dir
        os.makedirs(reports_dir, exist_ok=True)
    
    def generate(self, daily_reports: List[str], system_name: str) -> str:
        """
        توليد تقرير أسبوعي من التقارير اليومية
        
        Args:
            daily_reports: قائمة مسارات التقارير اليومية
            system_name: اسم النظام
        """
        today = datetime.datetime.now()
        year, week, _ = today.isocalendar()
        filename = f"{self.reports_dir}/{system_name}_{year}_W{week:02d}.txt"
        
        line = "=" * 70
        small_line = "-" * 70
        
        content = f"""
{line}
                    ENTRO-AI WEEKLY REPORT
{line}

System: {system_name}
Week: {year}-W{week:02d}
Period: {self._get_week_range(today)}
Generated: {today.strftime("%Y-%m-%d %H:%M:%S")}

{small_line}

📊 WEEKLY SUMMARY
{small_line}

Total Daily Reports: {len(daily_reports)}
Days with Alerts: {self._count_days_with_alerts(daily_reports)}
Total Interventions: {self._sum_metric(daily_reports, 'edt_interventions')}
Average Daily Risk: {self._avg_metric(daily_reports, 'avg_risk'):.3f}
Peak Risk (Week): {self._max_metric(daily_reports, 'peak_risk'):.3f}

{small_line}

📈 DAILY TREND
{small_line}

{self._generate_trend_chart(daily_reports)}

{small_line}

📋 WEEKLY RECOMMENDATIONS
{small_line}

{self._weekly_recommendations(daily_reports)}

{line}
ENTRO-AI Weekly Report | Builds on ENTROPIA DOI: 10.5281/zenodo.19416737
{line}
"""
        
        with open(filename, 'w', encoding='utf-8') as f:
            f.write(content)
        
        print(f"✅ Weekly report saved: {filename}")
        return filename
    
    def _get_week_range(self, date: datetime.datetime) -> str:
        """الحصول على نطاق الأسبوع"""
        start = date - datetime.timedelta(days=date.weekday())
        end = start + datetime.timedelta(days=6)
        return f"{start.strftime('%Y-%m-%d')} to {end.strftime('%Y-%m-%d')}"
    
    def _count_days_with_alerts(self, reports: List[str]) -> int:
        """حساب عدد الأيام التي بها تنبيهات"""
        # تبسيط: في التطبيق الحقيقي نقرأ الملفات
        return len(reports) // 2  # placeholder
    
    def _sum_metric(self, reports: List[str], metric: str) -> int:
        """جمع مقياس معين"""
        return len(reports) * 2  # placeholder
    
    def _avg_metric(self, reports: List[str], metric: str) -> float:
        """متوسط مقياس معين"""
        return 0.35  # placeholder
    
    def _max_metric(self, reports: List[str], metric: str) -> float:
        """أقصى مقياس معين"""
        return 0.89  # placeholder
    
    def _generate_trend_chart(self, reports: List[str]) -> str:
        """توليد رسم بياني نصي للاتجاه"""
        # تبسيط: رسم بياني بسيط
        chart = ""
        for i in range(min(7, len(reports))):
            risk = 0.3 + (i * 0.1)
            bar_length = int(risk * 40)
            bar = "█" * bar_length
            chart += f"  Day {i+1}: {bar} {risk:.2f}\n"
        return chart
    
    def _weekly_recommendations(self, reports: List[str]) -> str:
        """توصيات أسبوعية"""
        return """
    1. Review peak risk days for patterns
    2. Check if EDT interventions reduced risk effectively
    3. Compare with previous week's metrics
    4. Schedule maintenance if trend is upward
"""


